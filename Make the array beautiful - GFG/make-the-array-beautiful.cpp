class Solution {
  public:
    vector<int> makeBeautiful(vector<int> v) {
        stack<int> st;
        for (int i = 0; i < v.size(); i++)
    	{
    		while(i<v.size() && !st.empty() && ((st.top()<0 && v[i]>=0) || (st.top()>=0 && v[i]<0))){
    			i++; st.pop();
    		}
    		if(i<v.size()) st.push(v[i]);
    	}
        vector<int> ans;
        while(!st.empty()){
            ans.push_back(st.top());
            st.pop();
        }
        reverse(ans.begin(),ans.end());
        return ans;
    }
};

