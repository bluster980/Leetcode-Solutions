class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int n)
    {
        // code here.. 
        stack<int> x;
        int i=n,m=(n+1)/2;
        while(i>=m)
        {
            x.push(s.top());
            s.pop();
            i--;
        }
        x.pop();
        while(!x.empty())
        {
            s.push(x.top());
            x.pop();
        }
    }
};

